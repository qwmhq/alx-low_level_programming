#include "3-calc.h"

/**
 * op_add - add two integers
 * @a: integer
 * @b: integer
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract one integer from another
 * @a: integer
 * @b: integer
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two integers
 * @a: integer
 * @b: integer
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide one integer by another
 * @a: integer
 * @b: integer
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - find remainder of the division of one integer by another
 * @a: integer
 * @b: integer
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
