/**
 * add - add two integers
 *
 * @a: int
 * @b: int
 *
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - find the difference between two integers
 *
 * @a: int
 * @b: int
 *
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}


/**
 * mul - multiply two integers
 *
 * @a: int
 * @b: int
 *
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two integers
 *
 * @a: int
 * @b: int
 *
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - find the modulo of two integers
 *
 * @a: int
 * @b: int
 *
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
